package sec03_JavaFX_레이아웃.exam01_programmatical_layout;

import javafx.application.Application;
import javafx.collections.ObservableList;
import javafx.geometry.Insets;
import javafx.scene.Scene;
import javafx.scene.control.Button;
import javafx.scene.control.TextField;
import javafx.scene.layout.HBox;
import javafx.stage.Stage;

public class AppMain extends Application{
	@Override
	public void start(Stage primaryStage) throws Exception {
		HBox hbox = new HBox();							//Parent의 하위 클래스인 HBox() 컨테이너 생성
		hbox.setPadding( new Insets(10,10,10,10));		//안쪽 여백 설정(Padding)
		hbox.setSpacing(10);							//컨트롤간의 수평 간격 설정
		
		TextField textField = new TextField();			//TextField 컨트롤 생성
		textField.setPrefWidth(200);					//TextField의 폭 설정
		
		Button button = new Button();					//Button 컨트롤 생성
		button.setText("확인버튼"); 						//Button의 글자 설정
		
		ObservableList list = hbox.getChildren();		//HBox의 ObservableList 얻기
		list.add(textField); list.add(button);			//TextField와 Button 컨트롤 배치
		
		Scene scene = new Scene(hbox);					//Parent의 하위클래스인 HBox를 루트 컨테이너로써(매개값)으로 저장
		
		primaryStage.setTitle("AppMain타이틀");			//윈도우 창 제목 설정
		primaryStage.setScene(scene);					//윈도우 창에 화면 설정
		primaryStage.show();							//윈도우 창 보여주기
	}
	
	public static void main(String[] args) {
		launch(args);
	}
}
