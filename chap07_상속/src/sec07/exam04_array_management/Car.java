package sec07.exam04_array_management;

public class Car {
	Tire[] tires = {
			new Tire("앞왼쪽",5),
			new Tire("앞오른쪽",10),
			new Tire("뒤왼쪽",7),
			new Tire("뒤오른쪽",6)
	};
	//메소드
	int run() {
		System.out.println("[자동차가 달립니다]");
		for(int i =0; i < tires.length; i++) {
			if(tires[i].roll() == false) {
				stop();
				return (i+1);
			}
		}
		return  0;
	}
	void stop( ) {
		System.out.println("[자동차가 멈춥니다.]");
	}
}